using System;

namespace SumOf3Numbers
{
    class SumOf3Numbers
    {
        static void Main()
        {
            int a, b, c;

            a = Convert.ToInt32(Console.ReadLine());
            b = Convert.ToInt32(Console.ReadLine());
            c = Convert.ToInt32(Console.ReadLine());

            Console.WriteLine(a + b + c);
        }
    }
}
